#include <object.h>

void Object::setX(double x) {
	this->x = x;
}
double Object::getX() {
	return this->x;
}
void Object::setY(double y) {
	this->y = y;
}
double Object::getY() {
	return this->y;
}
void Object::setAngle(double angle) {
	this->angle = angle;
}
double Object::getAngle() {
	return this->angle;
}
